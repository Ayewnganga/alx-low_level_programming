/**
 * op_add - add
 * @a: integer
 * @b: integer
 *
 * Return: results
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract
 * @a: integer
 * @b: integer
 *
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: integer
 * @b: integer
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - modulus for remainder
 * @a: integer
 * @b: integer
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
